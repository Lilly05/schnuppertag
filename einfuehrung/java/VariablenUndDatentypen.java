
public class VariablenUndDatentypen {

    public static void main(String[] args) {
        // 2_1_Java_Variablen_und_Datentypen Aufgaben
        int age = 15;
        int height = 50;
        height = height + 112;
        int weight = 49;
        String vorname = "Lilly";
        String nachname = "Koller";
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("Weight: " + weight);
        System.out.println("Name: " + vorname + " " + nachname);
    }
}
