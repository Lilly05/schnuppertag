public class Arrays {

    public static void main(String[] args) {
        String[] kolleginnen = new String[]{"Laura", "Nora", "Magali", "Chantal", "Fredi"};
        System.out.println(kolleginnen[3]);
        for (int i = 0; i <= 4; ++i) {
            System.out.println(kolleginnen[i]);
        }
    }
}
